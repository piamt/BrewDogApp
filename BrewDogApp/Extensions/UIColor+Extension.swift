import UIKit

extension UIColor {
    static func BrandCard() -> UIColor {
        return UIColor(red: 236.0 / 255.0, green: 207.0 / 255.0, blue: 92.0 / 255.0, alpha: 1.0)
    }
    
    static func BrandBG() -> UIColor {
        return UIColor(red: 112.0 / 255.0, green: 107.0 / 255.0, blue: 75.0 / 255.0, alpha: 1.0)
    }
    
    static func BrandBlack() -> UIColor {
        return UIColor(red: 104.0 / 255.0, green: 76.0 / 255.0, blue: 10.0 / 255.0, alpha: 1.0)
    }
}
